#ifndef HEADER_DH_H
#include <openssl/dh.h>
#endif
DH *get_dh512()
	{
	static unsigned char dh512_p[]={
		0xC9,0xCE,0x6A,0x17,0xC9,0x69,0x9F,0x1A,0x76,0xEF,0x1B,0x5F,
		0x8C,0xE7,0x70,0x0C,0xA9,0xDC,0x76,0xF8,0x6A,0x46,0xFF,0x63,
		0x45,0x44,0x2B,0x79,0xEC,0x44,0x4F,0xEC,0x27,0xEA,0x97,0x33,
		0xC0,0x0E,0x8D,0xE5,0x51,0x4A,0x7F,0x7E,0xE8,0xE2,0x95,0xCC,
		0x31,0x90,0x03,0x0D,0x0F,0x46,0x8C,0xC7,0x3E,0x74,0x33,0x19,
		0x4F,0x64,0x49,0x2B,
		};
	static unsigned char dh512_g[]={
		0x02,
		};
	DH *dh;

	if ((dh=DH_new()) == NULL) return(NULL);
	dh->p=BN_bin2bn(dh512_p,sizeof(dh512_p),NULL);
	dh->g=BN_bin2bn(dh512_g,sizeof(dh512_g),NULL);
	if ((dh->p == NULL) || (dh->g == NULL))
		{ DH_free(dh); return(NULL); }
	return(dh);
	}
